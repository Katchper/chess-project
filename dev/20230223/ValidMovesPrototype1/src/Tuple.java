public class Tuple {
    private int y;
    private int x;

    public Tuple(int y, int x){
        this.x = y;
        this.y = x;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
